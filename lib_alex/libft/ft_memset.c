#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	char	*s2;

	if (len)
	{
		s2 = (char *)s;
		while (len)
		{
			*s2++ = c;
			len--;
		}
	}
	return (s);
}
