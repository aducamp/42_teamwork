#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char		*a1;
	char		*a2;

	a1 = (char*)s1;
	a2 = (char*)s2;
	while (n--)
		*a1++ = *a2++;
	return (s1);
}
